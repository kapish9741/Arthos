interface SignInFlowProps {
  onSubmit?: (e: React.FormEvent) => void;
  email?: string;
  setEmail?: (value: string) => void;
  password?: string;
  setPassword?: (value: string) => void;
  error?: string;
  loading?: boolean;
}

export default function SignInFlow({
  onSubmit,
  email: propEmail,
  setEmail: propSetEmail,
  password: propPassword,
  setPassword: propSetPassword,
  error: propError,
  loading: propLoading
}: SignInFlowProps = {}) {
  const navigate = useNavigate();
  const [localEmail, setLocalEmail] = useState("");
  const [localPassword, setLocalPassword] = useState("");
  
  // Use props if provided, otherwise use local state
  const email = propEmail !== undefined ? propEmail : localEmail;
  const setEmail = propSetEmail || setLocalEmail;
  const password = propPassword !== undefined ? propPassword : localPassword;
  const setPassword = propSetPassword || setLocalPassword;
  const error = propError;
  const loading = propLoading;

  const handleSubmit = (e: React.FormEvent<HTMLFormElement>) => {
    e.preventDefault();
    if (onSubmit) {
      onSubmit(e);
    } else {
      // Default behavior if no onSubmit provided
      console.log("Login:", { email, password });
      navigate('/dashboard');
    }
  };

  return (
    <div className="min-h-screen bg-black relative">
      {/* Canvas Reveal Effect */}
      <div className="absolute inset-0 pointer-events-none">
        <CanvasRevealEffect
          animationSpeed={3}
          containerClassName="bg-black"
          colors={[
            [236, 72, 153],
            [232, 121, 249],
          ]}
          dotSize={2}
        />
      </div>

      {/* Aurora Background */}
      <AuroraBackground className="absolute inset-0">
        <motion.div
          initial={{ opacity: 0.0, y: 40 }}
          whileInView={{ opacity: 1, y: 0 }}
          transition={{
            delay: 0.3,
            duration: 0.8,
            ease: "easeInOut",
          }}
          className="relative flex flex-col gap-4 items-center justify-center px-4 h-screen"
        >
          <div className="max-w-md w-full mx-auto rounded-2xl p-8 shadow-input bg-black/50 backdrop-blur-xl border border-white/10 z-10">
            <h2 className="font-bold text-2xl text-white mb-2">
              Welcome Back
            </h2>
            <p className="text-neutral-300 text-sm mb-8">
              Sign in to your account to continue
            </p>

            <form className="space-y-6" onSubmit={handleSubmit}>
              {/* Error Message */}
              {error && (
                <div className="p-3 bg-red-500/10 border border-red-500/50 rounded-lg text-red-400 text-sm text-center">
                  {error}
                </div>
              )}

              <LabelInputContainer>
                <Label htmlFor="email">Email Address</Label>
                <Input
                  id="email"
                  placeholder="yourname@email.com"
                  type="email"
                  value={email}
                  onChange={(e) => setEmail(e.target.value)}
                  required
                />
              </LabelInputContainer>

              <LabelInputContainer>
                <Label htmlFor="password">Password</Label>
                <Input
                  id="password"
                  placeholder="••••••••"
                  type="password"
                  value={password}
                  onChange={(e) => setPassword(e.target.value)}
                  required
                />
              </LabelInputContainer>

              <button
                className="bg-gradient-to-br relative group/btn from-black to-neutral-600 block w-full text-white rounded-md h-11 font-medium shadow-[0px_1px_0px_0px_#ffffff40_inset,0px_-1px_0px_0px_#ffffff40_inset] hover:shadow-lg transition-all disabled:opacity-50 disabled:cursor-not-allowed"
                type="submit"
                disabled={loading}
              >
                {loading ? 'Signing in...' : 'Sign In →'}
                <BottomGradient />
              </button>

              <div className="text-center text-neutral-300 text-sm mt-4">
                Don't have an account?{" "}
                <a
                  href="/signup"
                  className="text-purple-400 hover:text-purple-300 transition-colors"
                >
                  Sign up
                </a>
              </div>
            </form>
          </div>
        </motion.div>
      </AuroraBackground>
    </div>
  );
}

